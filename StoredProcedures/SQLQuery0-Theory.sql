--	Хранимая процедура - это именованная последовательность выражений
--	и дугих конструкций языка SQL, реализующая определенный алгоритм.
--	Один раз написанная и сохраненная в Базу Данных процедура 
--	может быть вызвана сколько угодно раз.
----------------------------------------------------------------------
--	Функция - это именованная последовательность выражений 
--			  и других конструкций языка SQL, реализующая определенный алгоритм.
--	Один раз написанная и сохраненная в Базу Данных функция 
--	может быть вызвана сколько угодно раз.
----------------------------------------------------------------------
--	1.	Ключевое отличие между функцией и хранимой процедурой состоит в том,
--		что Хранимые процедуры поддерживают транзакции, а функции нет.
--		Транзакция - это последовательность действий, которая вносит данные в Базу.
--		Грубо говоря, процедуры могут изменять и добавлять данные в Базу, а функции не могут.
--	2.  Процедура может вызывать функции, но функция НЕ может вызывать другие процелуры;
--	3.	Процедура может изменять входные параметры, а фукнкция этого делать НЕ может;
--	4.	Функция в обязательном порядке возвращает одно и только одно значение,
--		процедура никогда НЕ возвращает никаких значений;
--	5.	Поскольку процедуры никогда НЕ возвращают никаких значений, они НЕ применимы
--		в конструкциях	SELECT, WHERE и HAVING;
--	6.	Процедуры поддерживают обработку исключений, а функции нет;